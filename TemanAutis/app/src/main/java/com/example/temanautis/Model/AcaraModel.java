package com.example.temanautis.Model;

public class AcaraModel {
    private int id,id_users;
    private String nama_acara,thumbnail,deskripsi_acara,tanggal_acara,jam_acara,tempat,link_acara;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_users() {
        return id_users;
    }

    public void setId_users(int id_users) {
        this.id_users = id_users;
    }

    public String getNama_acara() {
        return nama_acara;
    }

    public void setNama_acara(String nama_acara) {
        this.nama_acara = nama_acara;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public void setThumbnail(String thumbnail) {
        this.thumbnail = thumbnail;
    }

    public String getDeskripsi_acara() {
        return deskripsi_acara;
    }

    public void setDeskripsi_acara(String deskripsi_acara) {
        this.deskripsi_acara = deskripsi_acara;
    }

    public String getTanggal_acara() {
        return tanggal_acara;
    }

    public void setTanggal_acara(String tanggal_acara) {
        this.tanggal_acara = tanggal_acara;
    }

    public String getJam_acara() {
        return jam_acara;
    }

    public void setJam_acara(String jam_acara) {
        this.jam_acara = jam_acara;
    }

    public String getTempat() {
        return tempat;
    }

    public void setTempat(String tempat) {
        this.tempat = tempat;
    }

    public String getLink_acara() {
        return link_acara;
    }

    public void setLink_acara(String link_acara) {
        this.link_acara = link_acara;
    }
}
