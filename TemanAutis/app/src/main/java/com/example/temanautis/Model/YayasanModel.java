package com.example.temanautis.Model;

public class YayasanModel {
    private int id;
    private String nama_yayasan,alamat,no_telp,dokumentasi;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama_yayasan() {
        return nama_yayasan;
    }

    public void setNama_yayasan(String nama_yayasan) {
        this.nama_yayasan = nama_yayasan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getNo_telp() {
        return no_telp;
    }

    public void setNo_telp(String no_telp) {
        this.no_telp = no_telp;
    }

    public String getDokumentasi() {
        return dokumentasi;
    }

    public void setDokumentasi(String dokumentasi) {
        this.dokumentasi = dokumentasi;
    }
}
